"""
Experiment 4c — Static evaluation speed benchmark.

Measures evaluate_board() latency for BoardEvaluatorTrad and BoardEvaluatorNN
on a large set of positions (default 10k generated by random play).

Output:
  exp4c_speed_results.csv     — per-call timings + per-evaluator stats
  exp4c_speed_summary.txt     — summary table
  plots/exp4c_speed_box.png   — box plot of timings (log scale)
  plots/exp4c_speed_hist.png  — histogram per evaluator

Usage:
    python run_exp4c_eval_speed.py --stockfish <path>
    python run_exp4c_eval_speed.py --stockfish <path> --n 1000 --nn-depth 10
"""

import argparse
import random
import statistics
import sys
import time
from pathlib import Path

import chess
import numpy as np
import pandas as pd

sys.path.insert(0, str(Path(__file__).resolve().parent.parent.parent))

import argparse as _ap


def make_eval_args(stockfish_path: str, depth: int) -> _ap.Namespace:
    args = _ap.Namespace()
    args.depth_white_stockfish = depth
    args.depth_black_stockfish = depth
    args.skill_white = None
    args.skill_black = None
    args.stockfish_path = stockfish_path
    args.debug = False
    return args


def load_openings(path: Path) -> list[str]:
    fens = []
    for line in path.read_text(encoding='utf-8-sig').splitlines():
        line = line.strip()
        if not line or line.startswith('#'):
            continue
        fen = line.split('#', 1)[0].strip()
        if fen:
            fens.append(fen)
    return fens


def generate_positions(openings: list[str], n: int, rng: random.Random) -> list[str]:
    """Generates n positions by random play from openings."""
    positions = []
    while len(positions) < n:
        start = rng.choice(openings) if openings else chess.STARTING_FEN
        board = chess.Board(start)
        # Play 0..40 random plies
        max_plies = rng.randint(0, 40)
        for _ in range(max_plies):
            legal = list(board.legal_moves)
            if not legal:
                break
            board.push(rng.choice(legal))
        if board.is_game_over(claim_draw=True):
            continue
        positions.append(board.fen())
    return positions


def benchmark(evaluator, positions: list[str], name: str,
              warmup: int = 5) -> dict:
    """Times evaluate_board on each position; returns stats + raw timings."""
    print(f"Benchmarking {name} on {len(positions)} positions...")

    # Warmup
    for fen in positions[:warmup]:
        evaluator.evaluate_board(chess.Board(fen))

    timings = []
    for i, fen in enumerate(positions):
        board = chess.Board(fen)
        t0 = time.perf_counter()
        evaluator.evaluate_board(board)
        t1 = time.perf_counter()
        timings.append((t1 - t0) * 1_000_000)  # microseconds
        if (i + 1) % max(1, len(positions) // 10) == 0:
            print(f"  {name}: {i + 1}/{len(positions)}")

    arr = np.array(timings)
    return {
        'evaluator': name,
        'n': len(timings),
        'mean_us': round(float(np.mean(arr)), 2),
        'median_us': round(float(np.median(arr)), 2),
        'p95_us': round(float(np.percentile(arr, 95)), 2),
        'p99_us': round(float(np.percentile(arr, 99)), 2),
        'stdev_us': round(float(np.std(arr)), 2),
        'min_us': round(float(np.min(arr)), 2),
        'max_us': round(float(np.max(arr)), 2),
        'raw_timings_us': timings,
    }


def main():
    parser = argparse.ArgumentParser(description='Exp 4c — Evaluation speed benchmark')
    parser.add_argument('--stockfish', type=str, required=True)
    parser.add_argument('--n', type=int, default=10000, help='Number of test positions')
    parser.add_argument('--nn-depth', type=int, default=10)
    parser.add_argument('--seed', type=int, default=42)
    parser.add_argument('--openings-file', type=str, default='')
    parser.add_argument('--output-dir', type=str, default='',
                        help='Output directory (default: script directory)')
    args = parser.parse_args()

    script_dir = Path(__file__).resolve().parent
    engine_dir = script_dir.parent.parent
    output_dir = Path(args.output_dir) if args.output_dir else script_dir
    output_dir.mkdir(parents=True, exist_ok=True)

    openings_file = Path(args.openings_file) if args.openings_file else \
        engine_dir / 'experiments' / 'openings_eco25.fen'

    openings = load_openings(openings_file) if openings_file.exists() else []
    if openings:
        print(f"Loaded {len(openings)} opening FENs")

    rng = random.Random(args.seed)
    print(f"Generating {args.n} random positions...")
    positions = generate_positions(openings, args.n, rng)

    # Initialize evaluators
    print(f"Initializing BoardEvaluatorTrad and BoardEvaluatorNN (depth={args.nn_depth})...")
    from board_evaluator_trad import BoardEvaluatorTrad
    from board_evaluator_nn import BoardEvaluatorNN

    eval_args = make_eval_args(args.stockfish, args.nn_depth)
    trad = BoardEvaluatorTrad(eval_args, chess.WHITE)
    nn = BoardEvaluatorNN(eval_args, chess.WHITE)

    # Run benchmarks
    trad_stats = benchmark(trad, positions, 'TRAD')
    nn_stats = benchmark(nn, positions, 'NN')

    # Save per-call timings (compressed view)
    detail_rows = []
    for evaluator_name, stats in [('TRAD', trad_stats), ('NN', nn_stats)]:
        for i, us in enumerate(stats['raw_timings_us']):
            detail_rows.append({'evaluator': evaluator_name, 'position_idx': i, 'time_us': us})
    detail_df = pd.DataFrame(detail_rows)
    detail_csv = output_dir / 'exp4c_speed_results.csv'
    detail_df.to_csv(detail_csv, index=False)
    print(f"\nSaved: {detail_csv.name} ({len(detail_df)} timings)")

    # Summary
    summary_df = pd.DataFrame([
        {k: v for k, v in s.items() if k != 'raw_timings_us'}
        for s in [trad_stats, nn_stats]
    ])
    summary_csv = output_dir / 'exp4c_speed_summary.csv'
    summary_df.to_csv(summary_csv, index=False)
    print(f"Saved: {summary_csv.name}")

    print("\n--- Speed benchmark summary ---")
    print(summary_df.to_string(index=False))

    # Speed ratio
    ratio = nn_stats['mean_us'] / trad_stats['mean_us'] if trad_stats['mean_us'] > 0 else float('nan')
    print(f"\nNN is {ratio:.1f}x slower than TRAD on average")

    # Plots
    try:
        import matplotlib
        matplotlib.use('Agg')
        import matplotlib.pyplot as plt
    except ImportError:
        print("matplotlib not available — skipping plots")
        return

    plots_dir = output_dir / 'plots'
    plots_dir.mkdir(exist_ok=True)

    fig, ax = plt.subplots(figsize=(8, 5))
    data = [trad_stats['raw_timings_us'], nn_stats['raw_timings_us']]
    ax.boxplot(data, tick_labels=['TRAD', 'NN'], showfliers=False)
    ax.set_ylabel('Time per evaluate_board call (μs)')
    ax.set_yscale('log')
    ax.set_title('Evaluation latency distribution')
    ax.grid(True, alpha=0.3, which='both', axis='y')
    fig.tight_layout()
    fig.savefig(plots_dir / 'exp4c_speed_box.png', dpi=150)
    plt.close(fig)

    fig, ax = plt.subplots(figsize=(10, 5))
    bins = np.logspace(np.log10(max(1, min(trad_stats['min_us'], nn_stats['min_us']))),
                       np.log10(max(trad_stats['max_us'], nn_stats['max_us'])), 50)
    ax.hist(trad_stats['raw_timings_us'], bins=bins, alpha=0.5, label='TRAD', color='#1976D2')
    ax.hist(nn_stats['raw_timings_us'], bins=bins, alpha=0.5, label='NN', color='#D32F2F')
    ax.set_xscale('log')
    ax.set_xlabel('Time (μs)')
    ax.set_ylabel('Count')
    ax.set_title('Histogram of evaluation latencies')
    ax.legend()
    ax.grid(True, alpha=0.3, which='both')
    fig.tight_layout()
    fig.savefig(plots_dir / 'exp4c_speed_hist.png', dpi=150)
    plt.close(fig)

    # Text summary
    txt_path = output_dir / 'exp4c_speed_summary.txt'
    with open(txt_path, 'w', encoding='utf-8') as f:
        f.write('Experiment 4c — Evaluation speed benchmark\n')
        f.write('=' * 60 + '\n\n')
        f.write(f'Positions: {args.n}\n')
        f.write(f'NN depth (Stockfish): {args.nn_depth}\n\n')
        f.write(summary_df.to_string(index=False) + '\n\n')
        f.write(f'NN is {ratio:.1f}x slower than TRAD on average.\n')
        f.write(f'TRAD throughput: {1_000_000 / trad_stats["mean_us"]:.0f} evals/s\n')
        f.write(f'NN   throughput: {1_000_000 / nn_stats["mean_us"]:.0f} evals/s\n')
    print(f"Saved: {txt_path.name}")

    print(f"Plots saved to: {plots_dir}")
    print("Done.")


if __name__ == '__main__':
    main()
